package com.cmpt276.groupproject.models;

public class quote {
    
    private int c;
    private int d;
    private int dp;
    private int h;
    private int l;
    private int o;
    private int pc;

    

    public quote() {
    }


    public quote(int c, int d, int dp, int h, int l, int o, int pc) {
        this.c = c;
        this.d = d;
        this.dp = dp;
        this.h = h;
        this.l = l;
        this.o = o;
        this.pc = pc;
    }


    public int getC() {
        return c;
    }


    public void setC(int c) {
        this.c = c;
    }


    public int getD() {
        return d;
    }


    public void setD(int d) {
        this.d = d;
    }


    public int getDp() {
        return dp;
    }


    public void setDp(int dp) {
        this.dp = dp;
    }


    public int getH() {
        return h;
    }


    public void setH(int h) {
        this.h = h;
    }


    public int getL() {
        return l;
    }


    public void setL(int l) {
        this.l = l;
    }


    public int getO() {
        return o;
    }


    public void setO(int o) {
        this.o = o;
    }


    public int getPc() {
        return pc;
    }


    public void setPc(int pc) {
        this.pc = pc;
    }

    
    

}
